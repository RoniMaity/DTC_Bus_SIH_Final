      <h2>History of DTC</h2>
      <h3>Overview</h3>
  <p>Government of India, Ministry of Transport took over the local bus services of Delhi in May 1948 in the name of Delhi Transport Service when they found that the services offered by Gwalior and Northern India Transport Company Ltd., the then licensee, were inadequate. A Delhi Road Transport Authority was constituted under the Road Transport Corporation Act, 1950. This Authority became undertaking of Municipal Corporation of Delhi by an Act of Parliament in April, 1958.
  On the recommendation of a Working Group of Planning Commission which concluded that Delhi Transport as an extension of Municipal Corporation of Delhi had not been functioning efficiently and adequately resulting in leakage of revenue and very high operation cost, Govt. of India took over the management of the undertaking by passing the Delhi Road Transport Laws (Amendment) Act, in 1971. It took over the assets and liabilities from the erstwhile Delhi Transport Undertaking (DTU) operated by the Municipal Corporation of Delhi till 2nd Nov,1971. Thus the DTC was set up in 1971.</p>
  <p>In section 22 of the Road Transport Corporation Act, 1950, following objectives were laid down for the DTC:</p>
  <ul>
    
  <li style="text-align: left">To provide or secure or promote an efficient, economical, reliable and properly coordinated system the road transport in Union Territory of Delhi and any extended area.</li>
  <li style="text-align: left;">In doing so, it shall act on business principles.</li>
  <li style="text-align: left">To achieve a high level operational efficiency.</li>
  <li style="text-align: left">To charge fares not exceeding those prescribed by the State Under Section 43(1)(i) of the Motor Vehicle Act, 1939.</li>
  <li style="text-align: left">To attain financial self-sufficiency</li>.
  <li style="text-align: left">DTC which was functioning under the administrative control of Govt. of India, was finally taken over by Govt. of NCT, Delhi on 05.08.1996.</li>
  
  </ul>
  

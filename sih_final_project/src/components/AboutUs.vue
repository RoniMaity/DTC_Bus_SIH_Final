  <h2>Depots</h2>
  <p>DTC operates its buses from 37 bus depots categorized into 4 regions: East, North, South & West. Each region is headed by a Regional Manager, and each depot is headed by a Depot Manager. 36 of these depots are located in Delhi, with 1 located in the neighboring city of Noida. These facilities perform regular maintenance and cleaning of buses. To complement DTC's existing depots, Delhi government is constructing 9 new bus depots at Kirari, East Vinod Nagar 2, Narela, Daurala, Burari, Savda Ghevra, Kapashera, Gadaipur, and Chattarpur</p>
  <h2>Fact</h2>
  <p>As of September 2023, DTC has a fleet of 4,088 buses, with 3,288 CNG buses and 800 electric buses. This makes its fleet the largest CNG bus fleet in the world, and the largest electric bus fleet in India. The buses are dispatched from 37 bus depots across Delhi. With Delhi's having a total of 7,135 buses, this means that 57% of these belong to DTC.</p>

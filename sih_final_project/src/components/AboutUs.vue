    <h2>Services</h2>
    <p>DTC has an extensive network of bus routes spread all over Delhi. A few routes also go into the neighboring cities of Noida, Ghaziabad, Gurgaon, and Faridabad. As of June 2023, of the 606 total bus routes in Delhi, 259 were operated by DTC, 209 were operated by DIMTS, and 138 routes were operated on by both the authorities under the Department of Transport's 'Unified Time Table'.</p><b>These routes are serviced by 7,135 buses, with 4,088 of them under DTC, out of 37 depots and 3 ISBTs</b> .<p> The average number of trips in FY 2021-22 was 31,084 per day. The number of routes being operated by the corporation has been steadily falling over the years under the Delhi government's policy of a 50:50 ratio of DTC buses and cluster buses on every route.
  
  DTC also provides various other services like night services, tourist services, and airport express services. It also provides buses to Delhi Police and used to do so for some schools (however, this was stopped in July 2022)</p>
